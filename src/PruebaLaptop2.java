    public DefaultListModel agregarDato() {
        String marca = textoMarca.getText();
        String modeloLaptop = textoModelo.getText();
        int memoria = Integer.parseInt(textoMemoria.getText());
        double precio = Double.parseDouble(textoPrecio.getText());
        Laptop valores = new Laptop(marca, modeloLaptop, memoria, precio);
        String valoresTexto = valores.toString();
        DefaultListModel modelo = (DefaultListModel) lista.getModel();
        modelo.addElement(valoresTexto);
        return modelo;
    }

